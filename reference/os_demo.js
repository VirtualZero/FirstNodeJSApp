const os = require('os')

// Platform
console.log(os.platform())

// CPU Arch
console.log(os.arch())

// CPU Cores
console.log(os.cpus())

// Free Memory
console.log(os.freemem())

// Total Memory
console.log(os.totalmem())

// Home directory
console.log(os.homedir())

// UpTime
console.log(os.uptime())